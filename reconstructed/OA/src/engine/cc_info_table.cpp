/*
 * cc_info_table.cpp  -  CSTGCCInfo::sCCInfoTable / sNumVoiceModelCCs (sec
 * 10.161).
 *
 * Real ground truth (`.text+0x277d0`, symbol "global constructors keyed
 * to CSTGCCInfo::sCCInfoTable", 7734 bytes) is a C++ dynamic global
 * initializer that runs once at module load. Confirmed via a full
 * disassembly + relocation dump (2219 instructions: 1080 `movb`, 18
 * `mov` register-byte stores, 18 `lea`, 1 `movzbl`, 1 `ret` -- NO
 * branches, NO calls anywhere in the function) that its entire effect
 * reduces to a single deterministic byte array:
 *
 *   - `sNumVoiceModelCCs` is read exactly once, at entry (`movzbl`) --
 *     guaranteed 0 at that point (`.bss` starts zeroed, and a whole-
 *     binary relocation scan found no other writer that could run
 *     first).
 *   - A chain of `lea N(%eax),%edx` (N = 1..0x12) computes 18
 *     sequential values 0..17 from that initial 0, each written
 *     (`mov %dl,OFFSET`) into the `+9` byte of one specific table
 *     entry -- CC 1, 2, 5, 7, 11, 16, 64, 65, 70, 71, 72, 73, 74, 75,
 *     76, 77, 78, 79, in that exact order (confirmed by walking the
 *     relocation list in address order, not assumed).
 *   - The final computed value (18 = 0x12) is written back to
 *     `sNumVoiceModelCCs` in the function's last instruction before
 *     `ret`.
 *   - Every other write is a plain literal `movb $imm,OFFSET` into
 *     `sCCInfoTable`.
 *
 * A small Python simulator (parses `objdump -dr`'s instruction +
 * relocation stream, tracks the two registers involved) replayed all
 * 1098 writes and confirmed: zero offset collisions (every one of the
 * table's 1200 bytes is written AT MOST once), and exactly 102 bytes
 * are never written at all (left at their `.bss` zero default -- NOT
 * filled with any sentinel). Cross-checked against real MIDI CC
 * conventions for extra confidence: CC7 (volume) and CC11 (expression)
 * both read back 0x7f=127 (full-scale default), CC10 (pan) reads 0x40=64
 * (the standard center value), CC64 (sustain) reads 0 (off).
 *
 * Since the whole computation is provably a single deterministic byte
 * array (not merely "probably safe to approximate as one"), it's
 * reproduced here as a plain initialized array rather than 1098 lines
 * of transliterated store instructions -- both are confirmed
 * behaviorally identical, and the array form is vastly more reviewable
 * and correctly BYTE-EXACT (verified against the simulator's own
 * output, not hand-copied). See oa_global.h for the full class-level
 * documentation (field semantics, the two confirmed consumers).
 */

